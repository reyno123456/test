/**
* @brief	 
* @param  	 
* @retval 	
* @note   
*/
void OV5640_init(void)
{
	uint32_t uint32_t_arSize = (sizeof(OV5640_init_setting_30fps_VGA)/sizeof(OV5640_init_setting_30fps_VGA[0]));
	
	I2C_Init(OV5640_COMPONENT,I2C_Master_Mode,OV5640_I2C_ADDR, I2C_Standard_Speed);
	
	OV5640_download_firmware(OV5640_init_setting_30fps_VGA,uint32_t_arSize);
}

/**
* @brief	 download OV5640 settings to sensor through i2c
* @param  	 
* @retval 	
* @note   
*/
uint32_t OV5640_download_firmware(struct reg_value *pModeSetting, uint32_t ArySize)
{
	register uint32_t Delay_ms = 0;
	register uint16_t RegAddr = 0;
	register uint8_t Mask = 0;
	register uint8_t Val = 0;
	uint8_t RegVal = 0;
	uint32_t i, retval = 0;

	for (i = 0; i < ArySize; ++i, ++pModeSetting) {
		Delay_ms = pModeSetting->uint32_tDelay_ms;
		RegAddr = pModeSetting->uint16_tRegAddr;
		Val = pModeSetting->uint8_tVal;
		Mask = pModeSetting->uint8_tMask;

		if (Mask) {
			retval = OV5640_read_reg(RegAddr, &RegVal);
			if (retval < 0)
				goto err;

			RegVal &= ~(uint8_t)Mask;
			Val &= Mask;
			Val |= RegVal;
		}

		retval = OV5640_write_reg(RegAddr, Val);
		if (retval < 0)
			goto err;

		if (Delay_ms)
			OV5640_delay(Delay_ms);
	}
err:
	return retval;
}
