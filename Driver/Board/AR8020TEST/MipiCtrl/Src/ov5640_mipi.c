/**
* @brief	 
* @param  	 
* @retval 	
* @note   
*/
int32_t OV5640_get_light_freq(void)
{
	/* get banding filter value */
	int32_t temp, temp1, light_freq = 0;
	uint8_t tmp;

	temp = OV5640_read_reg(0x3c01, &tmp);

	if (temp & 0x80) {
		/* manual */
		temp1 = OV5640_read_reg(0x3c00, &tmp);
		if (temp1 & 0x04) {
			/* 50Hz */
			light_freq = 50;
		} else {
			/* 60Hz */
			light_freq = 60;
		}
	} else {
		/* auto */
		temp1 = OV5640_read_reg(0x3c0c, &tmp);
		if (temp1 & 0x01) {
			/* 50Hz */
			light_freq = 50;
		} else {
			/* 60Hz */
			light_freq = 60;
		}
	}
	return light_freq;
}

