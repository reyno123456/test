/**
* @brief	write OV5640 register with value val 
* @param  	reg	OV5640 register address
* @param  	val	data need to write  
* @retval 	0:write success, -1:write failed
* @note   
*/
int32_t OV5640_write_reg(uint16_t reg, uint8_t val)
{
	uint8_t auint8_tBuf[3] = {0};
	int32_t s32_result = 0;

	auint8_tBuf[0] = reg & 0xff;
	auint8_tBuf[1] = reg >> 8;
	auint8_tBuf[2] = val;

	if(I2C_Master_Write_Data(OV5640_COMPONENT,OV5640_I2C_ADDR,auint8_tBuf,2,&auint8_tBuf[2],1) == FALSE)
	{
		dlog_info("write reg error:reg=%x,val=%x", reg, val);
		s32_result = -1;
	}
	else
	{
		;
	}

	return s32_result;
}

/**
* @brief	read OV5640 register 
* @param  	reg	OV5640 register address
* @param  	val	read data  
* @retval 	0:read success, -1:read failed
* @note   
*/
int32_t OV5640_read_reg(uint16_t reg, uint8_t *val)
{
	uint8_t auint8_tRegBuf[2] = {0};
	uint8_t uint8_tRdVal = 0;
	int32_t s32_result = 0;
	
	auint8_tRegBuf[0] = reg & 0xff;
	auint8_tRegBuf[1] = reg >> 8;

	if(I2C_Master_Read_Data(OV5640_COMPONENT,OV5640_I2C_ADDR,auint8_tRegBuf,2,&uint8_tRdVal,1) == FALSE) 
	{
		dlog_info("read reg error:reg=%x", reg);
		s32_result = -1;
	}
	else
	{
		;
	}	

	*val = uint8_tRdVal;

	return s32_result;
}

